function mag_to_radius(mag, fov_deg, window_size) {
    const R_0_RELATIVE = 1.0 / 150.0; // Scales the stars linearly
    const R_0 = Math.max(1.0, R_0_RELATIVE * window_size);
    const N = 3.0; // How much should the radius of stars change with FOV; higher values of N cause smaller changes
    const O = 0.17; // How much should the radius change when changing the magnitude

    return R_0 * Math.log(180.0 / fov_deg * N) * Math.pow(10.0, -O * mag);
}

function project_star([x, y, z], fov_deg, window_size) {
    const outer_distance = Math.tan(deg_to_rad(fov_deg / 4.0));
    const x_p = x / (1.0 - z);
    const y_p = y / (1.0 - z);
    return [x_p / outer_distance * window_size, y_p / outer_distance * window_size];
}

function spherical_to_rectangular(dec, ra) {
    return [Math.cos(dec) * Math.sin(ra), Math.cos(dec) * Math.cos(ra), Math.sin(dec)];
}

function deg_to_rad(deg) {
    return deg / 180.0 * Math.PI;
}

function get_window_characteristic_dimension(window) {
    return Math.min(window.innerHeight, window.innerWidth) / 2.0; // Fit the whole FOV onto the screen; division by 2 is there because half of the screen is for negative projected x/y values and the other half is for positive ones
}

const FOV_DEG = 120.0;

document.addEventListener('DOMContentLoaded', (_event) => {
    const canvas = document.getElementById('background_canvas');
    const ctx = canvas.getContext('2d');

    canvas.width = window.innerWidth;
    canvas.height = window.innerHeight;

    let stars = [];

    function load_stars() {
        stars = [];
        for (const star_raw of STARS.split("\n")) {
            const [dec_deg, ra_deg, mag] = star_raw.trim().split(",").map((v) => parseFloat(v));
            const [dec, ra] = [deg_to_rad(dec_deg), deg_to_rad(ra_deg)];
            const [x, y, z] = spherical_to_rectangular(dec, ra);
            const window_size = get_window_characteristic_dimension(window);
            const [x_p, y_p] = project_star([x, y, z], FOV_DEG, window_size);
            stars.push({
                spherical: {
                    dec,
                    ra
                },
                rectangular: {
                    x, y, z
                },
                projected: {
                    x: x_p, y: y_p
                },
                radius: mag_to_radius(mag, FOV_DEG, window_size),
                mag
            });
        }
    }

    function draw_stars() {
        ctx.clearRect(0, 0, canvas.width, canvas.height);
        for (const star of stars) {
            ctx.fillStyle = "#dddddd90";
            ctx.beginPath();
            ctx.arc(star.projected.x + canvas.width / 2.0, star.projected.y + canvas.height / 2.0, star.radius, 0, Math.PI * 2);
            ctx.fill();
        }
    }

    load_stars();
    draw_stars();

    function debounce(fn, wait_ms = 120) {
        let t;
        return (...args) => { clearTimeout(t); t = setTimeout(() => fn(...args), wait_ms); };
    }

    function on_resize() {
        canvas.width = window.innerWidth;
        canvas.height = window.innerHeight;
        for (const star of stars) {
            const window_size = get_window_characteristic_dimension(window);
            star.radius = mag_to_radius(star.mag, FOV_DEG, window_size)
            let [x_p, y_p] = project_star([star.rectangular.x, star.rectangular.y, star.rectangular.z], FOV_DEG, window_size);
            star.projected.x = x_p;
            star.projected.y = y_p;
        }
        draw_stars();
    }

    window.addEventListener('resize', debounce(() => {
        on_resize();
    }, 80));
});

